# 42. Write a Python program to split a list into different variables. 

# Answer :

my_list = [10, 20, 30]
a, b, c = my_list
print("a =", a)
print("b =", b)
print("c =", c)

# Output :
# a = 10 
# b = 20 
# c = 30

# Exaplainnation :
# my_list me 3 elements hain
# a, b, c = my_list → list ke elements ko alag-alag variables
# print() → variables ke values ko print karta hai
