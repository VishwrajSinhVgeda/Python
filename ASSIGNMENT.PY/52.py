# 52 . How Do You Check the Presence of a Key in A Dictionary? 
# Answer :

student = {"name": "Rahul", "age": 20, "course": "Python"}

if "age" in student:
    print("Key exists")
else:
    print("Key does not exist")

# Output: 
# Key exists

# Explanation :

# You can use the 'in' keyword to check if a key exists in a dictionary.
