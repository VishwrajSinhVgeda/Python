# 44 .  Write a Python program to create a tuple with different data types.
# Answer :


my_tuple = (10, 3.14, "Python", True, [1, 2, 3])


print(my_tuple)

# Output: (10, 3.14, 'Python', True, [1, 2, 3])

# Explaintion :

# 10 → Integer
# 3.14 → Float
# "Python" → String
# True → Boolean
# [1, 2, 3] → List

#A tuple can store multiple values of different data types in a single variable, 
#and it is immutable (cannot be changed).