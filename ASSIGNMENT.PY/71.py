# 71 . What is File function in python? What are keywords to create 
# and write file. 
# Answer :

# In Python, the file function is used to open a file and returns a file object.
# The built-in open() function is used to create and write to files.
# The syntax is: open(filename, mode)
# 'filename' is the name of the file to be opened or created.
# 'mode' specifies the mode in which the file is opened.

# Common modes include:
# 'r' - Read (default mode)
# 'w' - Write (creates a new file or truncates an existing file)
# 'a' - Append (writes data to the end of the file)
# 'b' - Binary mode (used with other modes for binary files)    
# 't' - Text mode (default mode, used with other modes for text files)
