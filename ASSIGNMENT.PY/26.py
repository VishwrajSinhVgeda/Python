# 26. How will you remove last object from a list? 

# Answer: 

numbers = [10, 20, 30, 40, 50]
last_item = numbers.pop()

print("Hata hua item:", last_item)
print("List ab:", numbers)

# Output : Hata hua item: 50
# List ab: [10, 20, 30, 40]

