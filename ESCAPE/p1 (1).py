#normal print function
name1 = "tops tecnologies"
print(name1)

#use in backslash in print function then print double quotes
name2 = "\"tops technologies\""
print(name2)

#use in backslash in print function then print single quote
name3 = '\'tops technologies\''
print(name3)

#New line escape sequence
name4 = "tops\ntechnologies"
print(name4)

#Tab escape sequence
name5 = "tops\ttechnologies"
print(name5)

#Extra of 'end' 'Sep'
print("tops", end = " ")
print("technologies")

#Sep
print("tops" , "technologies" , "career" , "Center" ,sep = "\t")